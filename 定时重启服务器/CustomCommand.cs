namespace WinRemoteDesktop
{
    /// <summary>
    /// 自定义命令
    /// </summary>
    public class CustomCommand
    {
        /// <summary>
        /// 名称
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// 网址
        /// </summary>
        public string Url { get; set; }

        /// <summary>
        /// 可执行文件路径
        /// </summary>
        public string ExePath { get; set; }

        /// <summary>
        /// 消息
        /// </summary>
        public string Message { get; set; }
    }
}
